const CATS = Object.keys(BANK);

function pickRandom(arr, n) {
  const pool = arr.slice();
  const out = [];
  n = Math.min(n || 1, pool.length);
  for (let i = 0; i < n; i++) {
    const idx = Math.floor(Math.random() * pool.length);
    out.push(pool.splice(idx, 1)[0]);
  }
  return out;
}

exports.main = async (event) => {
  const { category, count } = event || {};
  let questions;
  if (category && category !== 'random' && BANK[category]) {
    questions = pickRandom(BANK[category], count || 1);
  } else {
    // 跨分类随机：先合并
    const all = CATS.reduce((a, k) => a.concat(BANK[k].map(q => ({ q, k }))), []);
    const picked = pickRandom(all, count || 1);
    questions = picked.map(x => x.q);
  }
  return { ok: true, questions };
};
