// 合并相同的单元格
const merges = [];
const colKeys = ['项目名称', '一级模块', '二级模块'];

colKeys.forEach((key, colIndex) => {
  let mergeStart = 1; // Excel中数据从第2行开始（第1行是表头）
  let prevValue = tasks[0][key];
  let hasMerge = false;

  for (let dataRow = 1; dataRow < tasks.length; dataRow++) {
    const currentValue = tasks[dataRow][key];
    if (currentValue !== prevValue || prevValue === '') {
      // 值不同或空值，检查是否需要合并之前的区间
      if (dataRow > mergeStart) {
        merges.push({
          s: { r: mergeStart, c: colIndex },
          e: { r: dataRow, c: colIndex }
        });
        hasMerge = true;
      }
      mergeStart = dataRow + 1;
      prevValue = currentValue;
    }
  }
  // 处理最后一段（包括所有值相同的情况）
  if (tasks.length >= mergeStart && (hasMerge || tasks.length > mergeStart)) {
    merges.push({
      s: { r: mergeStart, c: colIndex },
      e: { r: tasks.length, c: colIndex }
    });
  }
});

worksheet['!merges'] = merges;

